public class Arithmatic_Operations {
    public static void main(String[] args) {
        int intOperand = 10;
        double doubleOperand = 20.5;
        float floatOperand = 5.7f;

        double result1 = intOperand + doubleOperand;
        double result2 = doubleOperand * floatOperand;
        double result3 = intOperand + floatOperand;
        double result4 = intOperand * floatOperand;


        System.out.println("Result 1: " + result1);
        System.out.println("Result 2: " + result2);
        System.out.println("Result 3: " + result3);
        System.out.println("Result 4: " + result4);
    }
}
